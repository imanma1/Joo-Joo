package sample.apps.notifications.view.request;

import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.layout.AnchorPane;
import sample.view.Page;

import java.io.IOException;

public class RequestPage extends Page {
    public RequestPage(){
        try {
            FXMLLoader fxmlLoader = new FXMLLoader(getClass().getResource("RequestPage.fxml"));
            Parent root = fxmlLoader.load();
            this.pane = new AnchorPane(root);
            this.controller = fxmlLoader.getController();
        }
        catch (IOException e) {
            e.printStackTrace();
        }
    }
}
