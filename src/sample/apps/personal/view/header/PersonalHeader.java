package sample.apps.personal.view.header;

import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.layout.AnchorPane;
import sample.view.Page;

import java.io.IOException;

public class PersonalHeader extends Page {
    public PersonalHeader(){
        try {
            FXMLLoader fxmlLoader = new FXMLLoader(getClass().getResource("PersonalHeader.fxml"));
            Parent root = fxmlLoader.load();
            this.pane = new AnchorPane(root);
            this.controller = fxmlLoader.getController();
        }
        catch (IOException e) {
            e.printStackTrace();
        }
    }
}
